						<p class="uk18 color-secondary">
                            "O.I.R.P apporte son soutien à L'UNICEF dans le domaine de l'éducation dans son ensemble."
                        </p>
						
						<p>
							L’éducation favorise la mobilité socio-économique et constitue un moyen d’échapper à la pauvreté.
							Au cours de la dernière décennie, des progrès majeurs ont été réalisés pour améliorer l’accès à 
							l’éducation et accroître les taux de scolarisation à tous les niveaux, en particulier pour les filles.
							Néanmoins, plus de 260 millions d’enfants et d’adolescents n’étaient pas scolarisés en 2018 — soit 
							près d’un cinquième de la population mondiale dans ce groupe d’âge. En outre, plus de la moitié de
							tous les enfants et adolescents du monde n’ont pas le niveau minimal de compétence en lecture et
							en mathématiques.
                        </p>

						<div class="spacer-30"></div>
						<h3 class="color-secondary"> <span class="color-primary"> L'éducation </span> durable</h3>
                        <p>
							En 2020, alors que la pandémie de COVID-19 se propageait dans le monde entier, une majorité de pays a imposé
							 la fermeture temporaire des écoles ; plus de 91 % des élèves dans le monde ont été concernés. En avril 2020, 
							 la fermeture des écoles a touché près de 1,6 milliard d’enfants et de jeunes. Près de 369 millions d’enfants
							  qui dépendent normalement des repas scolaires comme source fiable de nutrition quotidienne ont dû se
							   tourner vers d’autres sources.                        
							</p>

						<div class="spacer-30"></div>
						<h3 class="color-secondary">Les initiatives <span class="color-primary">de l'</span>UNESCO</h3>
                        <p>
							Jamais dans l’histoire, autant d’enfants n’ont été déscolarisés en même temps ; cette situation a perturbé leur apprentissage et bouleversé leur vie, notamment en ce qui concerne les enfants les plus vulnérables et les plus marginalisés. La pandémie mondiale a de graves conséquences qui peuvent mettre en péril les progrès durement acquis dans le domaine de l’amélioration de la qualité de l’éducation au niveau mondial.
							l’UNESCO a mis en place une série d’initiatives qui comprennent le suivi au niveau mondial des fermetures nationales ou localisées des établissements scolaires.
                        </p>

						<div class="spacer-30"></div>
						<h3 class="color-secondary">Un <span class="color-primary">partenariat </span>multisectoriel</h3>
                        <p>
							En mars 2020, soucieuse d’assurer le bien-être des enfants ainsi que la continuité de l’apprentissage,
							 l’UNESCO a lancé la Coalition mondiale pour l’éducation COVID-19, un partenariat multisectoriel réunissant 
							 des organismes de l’ONU, des organisations de la société civile, des médias et des partenaires des technologies de 
							 l’information visant à concevoir des solutions innovantes et à les mettre en œuvre. Ensemble, ils s’efforcent d’aider 
							 les pays à relever les défis de la connectivité et du contenu et à faciliter les possibilités d’apprentissage inclusives 
							 pour les enfants et les jeunes en cette période de perturbation soudaine et sans précédent de l’éducation.
						</p>

                        <p><b>"L'éducation durable, fait partie des 17 Objectifs de Développement Durable de l'ONU."</b></p>

